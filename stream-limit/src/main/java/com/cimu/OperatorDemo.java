public class OperatorDemo {

    public static void main(String[] args) {
        System.out.println("10 % 3 =" + (10 % 3));
        System.out.println("-10 % 3 = " + (-10 % 3));
        System.out.println("10 % -3 = " + (10 % -3));
        System.out.println("-10 % -3 = " + (-10 % -3));
    }
}
